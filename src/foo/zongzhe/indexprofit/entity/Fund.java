package foo.zongzhe.indexprofit.entity;

public class Fund {
	String date;
	double priceOrigin;
	double priceBalanced;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public double getPriceOrigin() {
		return priceOrigin;
	}

	public void setPriceOrigin(double priceOrigin) {
		this.priceOrigin = priceOrigin;
	}

	public double getPriceBalanced() {
		return priceBalanced;
	}

	public void setPriceBalanced(double priceBalanced) {
		this.priceBalanced = priceBalanced;
	}
}
